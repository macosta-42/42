#include <stdlib.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int	i;
	int	*tab;

	i = 0;
	if (min >= max)
	{
		*range = NULL;
		return (0);
	}
	if (!(tab = malloc(sizeof(*tab) * (max - min))))
		return (-1);
	while (min < max)
		tab[i++] = min++;
	*range = tab;
	return (i);
}
