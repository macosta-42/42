#include "ft.h"

int	main(int argc, char **argv)
{
	if (argc == 4)
	{
		if (ft_strlen(argv[2]) != 1)
		{
			ft_putchar('0');
			ft_putchar('\n');
			return (0);
		}
		ft_calc(argv[1], *argv[2], argv[3]);
	}
	return (0);
}
