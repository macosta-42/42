char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
		return (str);
	while (str[i] != '\0')
	{
		if ((str[i] > 32) && (str[i] < 127))
		{
			if ((str[i] >= 'a') && (str[i] <= 'z'))
				str[i] = str[i] - ('a' - 'A');
		}
		i++;
	}
	return (str);
}
