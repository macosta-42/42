int	ft_sqrt(int nb)
{
	int	i;
	int demi;

	i = 0;
	demi = nb / 2;
	if (nb == 1)
		return (1);
	if (nb == 2)
		return (0);
	while (i <= demi)
	{
		if (i > 46340)
			return (0);
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
